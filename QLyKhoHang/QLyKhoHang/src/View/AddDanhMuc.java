    private void DanhMuc_tablesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DanhMuc_tablesMouseClicked
        int selectedRow = DanhMuc_tables.getSelectedRow();
        if (selectedRow != -1) {
            String maDanhMuc = DanhMuc_tables.getValueAt(selectedRow, 0).toString();
            String tenDanhMuc = DanhMuc_tables.getValueAt(selectedRow, 1).toString();

            jTextField1.setText(maDanhMuc);
            jTextField2.setText(tenDanhMuc);
        }
    }//GEN-LAST:event_DanhMuc_tablesMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int yes_no = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn không ?","Xác nhận xóa",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(yes_no == JOptionPane.YES_OPTION){
            if(jTextField1.getText().isEmpty() ) {
                JOptionPane.showMessageDialog(this,"Vui lòng nhập ID");
            } else {
                int maSp = Integer.parseInt(jTextField1.getText());
                boolean success = DanhMucDAO.deleteProduct(maSp);
                if (success) {
                    jTextField1.setText("");
                    jTextField2.setText("");
                }
                  clearTableJ();
                  updateTableJ();  
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       if(jTextField2.getText().isEmpty() || jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đủ thông tin");
        } else {
            int maSp = Integer.parseInt(jTextField1.getText());
            String tenSp = jTextField2.getText();
            
            boolean success = DanhMucDAO.updateProduct(maSp, tenSp);
            if (success) {
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null, "Sửa danh mục thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Sửa danh mục thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }  
            
              clearTableJ();
              updateTableJ();  
        }
    }//GEN-LAST:event_jButton4ActionPerformed

     private void clearTableJ() {
        DefaultTableModel model=(DefaultTableModel) DanhMuc_tables.getModel();
        model.setRowCount(0);
    }
    
    private void updateTableJ() {
        try {
            String query = "SELECT * FROM danhmuc";
            Connection cnt = Connect.getConnection();
            Statement stm = cnt.createStatement();
            ResultSet rs = stm.executeQuery(query);
            DefaultTableModel model=(DefaultTableModel) DanhMuc_tables.getModel();
            while (rs.next()){
                Object objList[] = {
                    rs.getInt("id"),
                    rs.getString("categpry_name"),
                };
                model.addRow(objList);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int idDanhMuc = Integer.parseInt(jTextField1.getText().trim());
            String nameDanhMuc = jTextField2.getText();
            DanhMuc danhmuc = new DanhMuc();
            danhmuc.setCategpryName(nameDanhMuc);
            danhmuc.setId(idDanhMuc);
    
            boolean success = DanhMucDAO.insertSanPham(danhmuc);
    
            if (success) {
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null, "Thêm danh mục thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Thêm danh mục  thất bại do trùng ID", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            jTextField1.setText("");
            JOptionPane.showMessageDialog(null, "ID danh mục không hợp lệ. Vui lòng nhập lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        clearTableJ();
        updateTableJ(); 
    }//GEN-LAST:event_jButton1ActionPerformed

